package configuracion;


public abstract class ObstaculoExplosivoConfig {
	private int anchoObstaculoExplosivo=10;
	private int altoObstaculoExplosivo=10;	
	
	private int posicionX=60;
	private int posicionY=60;
	
	
	
	
	
	
//	getters setters
	public int getAnchoObstaculoExplosivo() {
		return anchoObstaculoExplosivo;
	}
	public int getAltoObstaculoExplosivo() {
		return altoObstaculoExplosivo;
	}
	public int getPosicionX() {
		return posicionX;
	}
	public int getPosicionY() {
		return posicionY;
	}
	
	
	
	

	
}
