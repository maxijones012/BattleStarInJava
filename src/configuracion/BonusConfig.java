package configuracion;

public abstract class BonusConfig {
	private int ancho=10;
	private int alto=10;
	public int getAncho() {
		return ancho;
	}
	public void setAncho(int ancho) {
		this.ancho = ancho;
	}
	public int getAlto() {
		return alto;
	}
	public void setAlto(int alto) {
		this.alto = alto;
	}
	
	
	
	
}
